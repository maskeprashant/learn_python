# for loop

print("for loop from 0 to 9:")
for i in range(10):
    print(i)

mySkills = ["Python", "JavaScript", "React", "Node.js", "Next.js"]
print("\nMy Skills:")
for skill in mySkills:
    print(f"- {skill}")

# while loop
print("\nwhile loop from 0 to 9:")
i = 0
while i < 10:
    print(i)
    i += 1
print("while loop ended.")

fruits = ["Apple", "Banana", "Cherry", "Date", "Elderberry"]

print("\n fruits list:")
fruitsCount = len(fruits)
index = 0
while index < fruitsCount:
    print(f"- {fruits[index]}")
    index += 1

print("while loop ended.")

